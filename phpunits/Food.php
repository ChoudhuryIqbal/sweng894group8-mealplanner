require_once('MealPlanner.php');

///////////////////////////////////////////////////////////////////////////////
// Special $_REQUEST options
///////////////////////////////////////////////////////////////////////////////
$_REQUEST['edit'] = TRUE;

///////////////////////////////////////////////////////////////////////////////
// Script location
///////////////////////////////////////////////////////////////////////////////
define( '__LOCATION__' , '/foods/food/' );

///////////////////////////////////////////////////////////////////////////////
// Object abstraction class
///////////////////////////////////////////////////////////////////////////////
class Food extends MealPlanner
{
    function __construct()
    {
        $this->setLocation(__LOCATION__);
    }

    function updateFood($foodID, $name, $cost)
    {
        // Assign $_REQUEST environment variable options
        $_REQUEST['foodid'] = $foodID;
        $_REQUEST['name']   = $name;
        $_REQUEST['cost']   = $cost;

        // Run execution last (but before error-checking)
        $this->execute();
    }
}

